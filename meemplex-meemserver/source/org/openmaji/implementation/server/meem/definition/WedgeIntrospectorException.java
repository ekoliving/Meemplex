package org.openmaji.implementation.server.meem.definition;

/**
 * @author Peter
 */
public class WedgeIntrospectorException extends Exception
{
	private static final long serialVersionUID = -910320090L;
	
	public WedgeIntrospectorException()
	{
		super();
	}

	public WedgeIntrospectorException(String arg0)
	{
		super(arg0);
	}

	public WedgeIntrospectorException(Throwable arg0)
	{
		super(arg0);
	}

	public WedgeIntrospectorException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}
}
